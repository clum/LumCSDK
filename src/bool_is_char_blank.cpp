//------------------------------------------------------------------------------------------------------
//IS_SLASH	
//
//Test if a char is a slash '/' or not
//
//INPUT:	-char c
//
//OUTPUT:	-1 if c is a '/'
//			 0 if c is not a '/'
//------------------------------------------------------------------------------------------------------
bool is_slash(char c) {
	return(c=='/');
}



//------------------------------------------------------------------------------------------------------
//IS_NOT_SLASH	
//
//Test if a char is a slash '/' or not
//
//INPUT:		-char c
//
//OUTPUT:		-1 if c is not a '/'
//				-0 if c is a '/'
//------------------------------------------------------------------------------------------------------
bool is_not_slash(char c) {
	return(!(c=='/'));
}



